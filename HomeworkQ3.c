#include<stdio.h>
//Q) Write comments for programs a & b
int main(){
    int a,b;//Variables Declared 
    printf("Enter the value of a : ");
    scanf("%d",&a); //Format specifier and ampersand operator is used 
    printf("Enter the value of b : ");
    scanf("%d",&b);//Format specifier and ampersand operator is used 
    printf("The sum of a&b is : %d",a+b);//Format specifier and Arithmetic operator (+) is used 
    return 0;
}